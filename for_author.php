				  <li style="margin-top: 10px;"><a href="#">Figures should be uploaded in the correct orientation.</a></li>
				  <li style="margin-top: 10px;"><a href="#">Figure titles (max 15 words) and legends (max 300 words) should be provided in the main<br> manuscript, not in the graphic file.</a></li>
				  <li style="margin-top: 10px;"><a href="#">Figure keys should be incorporated into the graphic, not into the legend of the figure.</a></li>
				  <li style="margin-top: 10px;"><a href="#">Each figure should be closely cropped to minimize the amount of white space surrounding the<br> illustration. Cropping figures improves accuracy when placing the figure in combination with<br> other elements when the accepted manuscript is prepared for publication on our site. For<br> more information on individual figure file formats, see our detailed instructions.</a></li>
				  <li style="margin-top: 10px;"><a href="#">Individual figure files should not exceed 10 MB. If a suitable format is chosen, this file size is <br>adequate for extremely high quality figures.</a></li>
				  <li style="margin-top: 10px;"><a href="#"><span style="font-weight: bold;">Please note that it is the responsibility of the author(s) to obtain permission from the<br> copyright holder to reproduce figures (or tables) that have previously been published<br> elsewhere. </span> In order for all figures to be open access, authors must have permission from the<br> rights holder if they wish to include images that have been published elsewhere in non open<br> access journals. Permission should be indicated in the figure legend, and the original source<br> included in the reference list.</a></li>
