					<h5 class="head3">Figures on the web:</h5>
					<nav class="navbar8">
						<div>
						  <ul>
						  <li style="margin-top: 30px;"><a href="#">width of 600 pixels (standard), 1200 pixels (high resolution).</a></li>
						  <li style="margin-top: 30px; list-style: none;"><a href="#">Figures in the final PDF version:</a></li>
						  <li style="margin-top: 30px;"><a href="#">width of 85 mm for half page width figure</a></li>
						  <li style="margin-top: 10px;"><a href="#">width of 170 mm for full page width figure</a></li>
						  <li style="margin-top: 10px;"><a href="#">maximum height of 225 mm for figure and legend</a></li>
						  <li style="margin-top: 10px;"><a href="#">image resolution of approximately 300 dpi (dots per inch) at the final size</a></li>
						  <li style="margin-top: 50px;list-style: none; margin-left: -15px;"><a href="#">Figures should be designed such that all information, including text, is legible at these <br> dimensions. All lines should be wider than 0.25 pt when constrained to standard figure widths. <br> All fonts must be embedded.</a></li>
