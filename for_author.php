					<p class="head3">Figures are resized during publication of the final full text and PDF versions to conform to the<br> BioMed Central standard dimensions, which are detailed below.</p>
					<p class="head3">Figures on the web:</p>
					<nav class="navbar8">
						<div>
						  <ul>
						  <li style="margin-top: 30px;"><p>width of 600 pixels (standard), 1200 pixels (high resolution).</p></li>
						  <li style="margin-top: 10px; "><p>Figures in the final PDF version:</p></li>
						  <li style="margin-top: 10px;"><p>width of 85 mm for half page width figure</p></li>
						  <li style="margin-top: 10px;"><p>width of 170 mm for full page width figure</p></li>
						  <li style="margin-top: 10px;"><p>maximum height of 225 mm for figure and legend</p></li>
						  <li style="margin-top: 10px;"><p>image resolution of approximately 300 dpi (dots per inch) at the final size</p></li>
						  <li style="margin-top: 50px;list-style: none; margin-left: -15px;"><p>Figures should be designed such that all information, including text, is legible at these <br> dimensions. All lines should be wider than 0.25 pt when constrained to standard figure widths. <br> All fonts must be embedded.</p></li>
