            <h4 class="heading1">Perpetual Payment to Scientists (Authors):</h4>
            <ul>
                <li><b>40% of Total Revenue:</b> XANA will allocate 40% of the total revenue to support scientists whose work has been published. The payment will be structured as follows:
                    <ol>
                        <li><b>Initial Payment:</b> Authors will receive an initial share of the revenue (e.g., 50% of their portion) upon publication. This will cover the direct compensation for the publication of their article.</li>
                        <li><b>Ongoing Payments</b> The remaining 50% will be distributed periodically (quarterly or annually) based on the article's <b>ongoing impact</b>. This could be calculated based on factors such as:
                            <ul>
                                <li><b>Citations:</b> Articles with higher citation rates could earn a larger share.</li>
                                <li><b>Downloads and Viewership:</b> Higher download rates and visibility on the platform could trigger additional payments.</li>
                                <li><b>Altmetrics:</b> Usage of the paper in industry applications, social media mentions, or media coverage could also be factors in determining the payout.</li>
                            </ul>
                        </li>
                    </ol>
                </li>
                <li>This ensures that researchers are continually rewarded as their work garners attention and impact, long after the initial publication.</li>
            </ul>
