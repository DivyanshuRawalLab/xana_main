                website, and to identify <br> users returning to a website.</p>

            <p>Cookies may be either "persistent" cookies or "session" cookies. A persistent cookie consists of a text
                file sent by a web server <br> to a web browser, which will be stored by the browser and will remain valid
                until its set expiry date (unless deleted by the user <br> before the expiry date). A session cookie, on the
                other hand, will expire at the end of the user session, when the web browser <br> is closed.&nbsp;</p>

